    public void displayOldInfo(int empNum){
        this.empNum = empNum;
        currentEmp = getReferenceValueFromEmpNum(empNum); 
        
        employeeNumberTextField.setText(String.valueOf(currentEmp.getEmpNum()));
        firstNameTextField.setText(currentEmp.getFirstName());
        lastNameTextField.setText(currentEmp.getLastName());
        genderComboBox.setSelectedItem(currentEmp.getGender());
        workLocationComboBox.setSelectedItem(currentEmp.getWorkLoc());
        deductionRateTextField.setText(String.valueOf((int)currentEmp.getDeductRate()));
        hireDateMonthComboBox.setSelectedItem(String.valueOf(currentEmp.getHireDateMonth()));
        hireDateDayComboBox.setSelectedItem(String.valueOf(currentEmp.getHireDateDay()));
        hireDateYearComboBox.setSelectedItem(String.valueOf(currentEmp.getHireDateYear()));

        
        if(currentEmp instanceof FTE){
            fullTimeRadioButton.setSelected(true);
            fullTimeEmployeeLabel.setVisible(true);
            yearlySalaryLabel.setVisible(true);
            yearlySalaryTextField.setVisible(true);
            FTE theFTE = (FTE) currentEmp;
            yearlySalaryTextField.setText(String.valueOf((int)theFTE.getGrossSalary()));
            eT = 1;
        }
        else if(currentEmp instanceof PTE){
           partTimeRadioButton.setSelected(true);
           partTimeEmployeeLabel.setVisible(true);
           hourlyWageLabel.setVisible(true);
           hourlyWageTextField.setVisible(true);
           hoursPerWeekLabel.setVisible(true);
           hoursPerWeekTextField.setVisible(true);
           weeksPerYearLabel.setVisible(true);
           weeksPerYearTextField.setVisible(true);
           submitButton.setVisible(true);
           PTE thePTE = (PTE) currentEmp;
           hourlyWageTextField.setText(String.valueOf((int)thePTE.getHourlyWage()));
           hoursPerWeekTextField.setText(String.valueOf((int)thePTE.getHoursPerWeek()));
           weeksPerYearTextField.setText(String.valueOf((int)thePTE.getWeeksPerYear()));
           eT = 0;
    }
    }
    
