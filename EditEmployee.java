    public EmployeeInfo getReferenceValueFromEmpNum(int num){
        for(int i = 0; i<masterHashTable.buckets.length; i++){
            for(int j = 0; j<masterHashTable.buckets[i].size(); j++){
                EmployeeInfo currentEmp = masterHashTable.buckets[i].get(j);
                int currentEmpNum = currentEmp.getEmpNum();
                if(currentEmpNum  == num){
                    return currentEmp;
                }
            }
        }
        return null;
    }
    
    
    
